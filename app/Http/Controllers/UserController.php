    {
        //
        $user = User::firstOrCreate(['username' => $id]);
        $cookieExpires=1;
        Cookie::queue('user_id', $user->id, $cookieExpires, '', '',
            false, false);
        Cookie::queue('username', $user->username, $cookieExpires, '', '',
            false, false);

        return view('chat');
    }

